contabilidad(L) :-
  L = [CONTABILIDAD, 
       INGRESO,
       FACTURAS_DE_COMPRA,
       FACTURAS_DE_VENTA,
       FACTURA_ELECTRONICA,
       REGISTRO_DE_COSTOS,
       REGISTRO_DE_GASTOS,
       REGISTRO_DE_INGRESOS,
       REGISTRO_DE_PASIVOS,
       REGISTRO_DE_ACTIVOS,
       BALANCES,
       IVA,
       RETEIVA,
       RETEFUENTE,
       RETEICA,
       DECLARACION_DE_RENTA,
       REPORTES,
       CONCILIACION_BANCARIA,
       INVENTARIO,
       PERSONAL],
  fd_domain(L, 0, 1),
  CONTABILIDAD #= 1,
  % OBLIGATORIO
  CONTABILIDAD #= INGRESO,
  CONTABILIDAD #= FACTURAS_DE_COMPRA,
  CONTABILIDAD #= REGISTRO_DE_COSTOS,
  CONTABILIDAD #= REGISTRO_DE_GASTOS,
  CONTABILIDAD #= REGISTRO_DE_INGRESOS,
  CONTABILIDAD #= REGISTRO_DE_PASIVOS,
  CONTABILIDAD #= REGISTRO_DE_ACTIVOS,
  CONTABILIDAD #= BALANCES,
  CONTABILIDAD #= REPORTES,
  % OPCIONAL
  CONTABILIDAD #>= IVA,
  CONTABILIDAD #>= RETEIVA,
  CONTABILIDAD #>= RETEFUENTE,
  CONTABILIDAD #>= RETEICA,
  CONTABILIDAD #>= DECLARACION_DE_RENTA,
  CONTABILIDAD #>= FACTURAS_DE_VENTA,
  CONTABILIDAD #>= FACTURA_ELECTRONICA,
  CONTABILIDAD #>= CONCILIACION_BANCARIA,
  CONTABILIDAD #>= INVENTARIO,
  CONTABILIDAD #>= PERSONAL,
  % REQUIERE
  %BALANCES #==> REGISTRO_DE_PASIVOS,
  %BALANCES #==> REGISTRO_DE_ACTIVOS,
  FACTURA_ELECTRONICA #==> FACTURAS_DE_VENTA,
  IVA #==> FACTURAS_DE_VENTA,
  RETEIVA #==> FACTURAS_DE_VENTA,
  RETEFUENTE #==> FACTURAS_DE_VENTA,
  RETEICA #==> FACTURAS_DE_VENTA,
  RETEIVA #==> IVA,
  %DECLARACION_DE_RENTA #==> REGISTRO_DE_COSTOS,
  %DECLARACION_DE_RENTA #==> REGISTRO_DE_GASTOS,
  %DECLARACION_DE_RENTA #==> REGISTRO_DE_INGRESOS,
  %DECLARACION_DE_RENTA #==> REGISTRO_DE_PASIVOS,
  %DECLARACION_DE_RENTA #==> REGISTRO_DE_ACTIVOS,
  %DECLARACION_DE_RENTA #==> BALANCES,
  %CONCILIACION_BANCARIA #==> REGISTRO_DE_COSTOS,
  %CONCILIACION_BANCARIA #==> REGISTRO_DE_GASTOS,
  %CONCILIACION_BANCARIA #==> REGISTRO_DE_INGRESOS,
  %INVENTARIO #==> FACTURAS_DE_COMPRA,
  INVENTARIO #==> FACTURAS_DE_VENTA,
  fd_labeling(L).

start(L) :-
  findall(L, contabilidad(L), Semantics),
  length(Semantics, NumberOfProducts),
  write('Semantics: '), nl,
  write(Semantics), nl,
  write('Number of Products: '), write(NumberOfProducts).
